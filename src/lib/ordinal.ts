const ORDINALS_RU = [
  "первый",
  "второй",
  "третий",
  "четвёртый",
  "пятый",
  "шестой",
  "седьмой",
  "восьмой",
  "девятый",
  "десятый",
];

// "1" -> "первый", "2" -> "второй", ... дальше просто "11-й", "12-й" и т.д.
export function ordinalRu(n: number): string {
  return ORDINALS_RU[n - 1] ?? `${n}-й`;
}
